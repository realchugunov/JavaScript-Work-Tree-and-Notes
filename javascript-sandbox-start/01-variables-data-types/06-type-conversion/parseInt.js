// String to a Number

let amount = '10';

amount = parseInt(amount);

console.log(amount, typeof amount);

// Number to a String

let string = 3;

string = string.toString();

console.log(string, typeof string);

// Decimal String to a Number

let dec = '27.5';

dec = parseFloat(dec);

console.log(dec, typeof dec);

// Decimal Number to a String

let deci = 55.5;

deci = deci.toString();

console.log(deci, typeof deci);

// Number to boolean

let booleanVal = 7;

booleanVal = Boolean(booleanVal);

console.log(booleanVal, typeof booleanVal);